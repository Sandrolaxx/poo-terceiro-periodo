// package seu_pacote_aqui;

public class Vendedor {
    private String nome;
    private String cpf;

    public Vendedor(String nome, String cpf) {
        this.nome = nome;
        this.cpf = cpf;
    }

    // Getters
    public String getNome() {
        return nome;
    }

    public String getCpf() {
        return cpf;
    }
}
